    fun getPib(pays:String):Int{
        var liste = init()
        for (el in liste){
            if (pays.lowercase() == el.pays.lowercase()){
                if (el.pib / 1000 <= 1){
                    return 1
                }
                else{
                    return (el.pib/1000).toInt()
                }
            }
        }
        return 1
    }

    fun pibrandom():Int{
        var liste = init()
        var rand = nextInt(0, liste.size)
        return liste[rand].pib
    }
